const catalogs: Record<Locale, Record<MessageKey, string>> = {
  en,
  'zh-CN': zhCN,
}

export type Translate = (
  key: MessageKey,
  values?: Readonly<Record<string, string | number>>,
) => string

export function resolveLocale(
  preference: Locale | null,
  languages: readonly string[] = typeof navigator === 'undefined'
    ? []
    : navigator.languages,
): Locale {
  if (preference) return preference
  return languages.some((language) => /^zh(?:-|$)/i.test(language)) ? 'zh-CN' : 'en'
}

export function translate(
  locale: Locale,
  key: MessageKey,
  values: Readonly<Record<string, string | number>> = {},
): string {
  return Object.entries(values).reduce(
    (message, [name, value]) => message.replaceAll(`{${name}}`, String(value)),
    catalogs[locale][key],
  )
}

interface WorkspaceLocaleContextValue {
  locale: Locale
  t: Translate
}

const WorkspaceLocaleContext = createContext<WorkspaceLocaleContextValue>({
  locale: 'en',
  t: (key, values) => translate('en', key, values),
})

export function WorkspaceLocaleProvider({
  locale,
  children,
}: {
  locale: Locale
  children: ReactNode
}) {
  const value = useMemo<WorkspaceLocaleContextValue>(() => ({
    locale,
    t: (key, values) => translate(locale, key, values),
  }), [locale])

  return (
    <WorkspaceLocaleContext.Provider value={value}>
      {children}
    </WorkspaceLocaleContext.Provider>
  )
}

export function useWorkspaceLocale() {
  return useContext(WorkspaceLocaleContext)
}

